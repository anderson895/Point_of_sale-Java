    private void jbtnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPayActionPerformed
        // Validate input for jtxtDisplay
        String input = jtxtDisplay.getText().replaceAll("[^\\d.]", ""); // Remove non-digit characters
        if (!input.matches("\\d*\\.?\\d+")) {
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter a valid number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Check if cash is greater than or equal to the total
        double cash = Double.parseDouble(input);

        // Extract the numeric part from jtxtTotal (remove currency symbol and whitespace)
        String totalText = jtxtTotal.getText().replaceAll("[^\\d.]", "");
        double total = Double.parseDouble(totalText);

        if (cash < total) {
            JOptionPane.showMessageDialog(this, "Insufficient cash. Please enter an amount equal to or greater than the total.", "Payment Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Proceed with payment
        if (jtxtPayMethod.getSelectedItem().equals("Cash")) {
            Change();
        } else {
            jtxtChange.setText("");
            jtxtDisplay.setText("");
        }
    }//GEN-LAST:event_jbtnPayActionPerformed

