    private void scaleButtonIcon(javax.swing.JButton button, String imagePath) {
        // Load the original icon
        ImageIcon originalIcon = new ImageIcon(imagePath);

        // Get the button size
        int buttonWidth = button.getWidth();
        int buttonHeight = button.getHeight();

        // Scale the icon to fit the button
        Image scaledImage = originalIcon.getImage().getScaledInstance(buttonWidth, buttonHeight, Image.SCALE_SMOOTH);
        ImageIcon scaledIcon = new ImageIcon(scaledImage);

        // Set the scaled icon as the button's icon
        button.setIcon(scaledIcon);
    }
    
    private void updateTimeLabel() {
        // Get current time
        Calendar calendar = Calendar.getInstance();
        Date now = calendar.getTime();

        // Format time in 12-hour format with AM/PM indicator
        SimpleDateFormat timeFormat = new SimpleDateFormat("hh:mm:ss a");
        String formattedTime = timeFormat.format(now);

        // Update time label
        timeLabel.setText(formattedTime);
    }
    
    private void updateDateLabel() {
        // Get current date
        Calendar calendar = Calendar.getInstance();
        Date now = calendar.getTime();

        // Format date as "Day, DD-MM-YYYY"
        SimpleDateFormat dateFormat = new SimpleDateFormat("EEEE, dd-MM-yyyy");
        String formattedDate = dateFormat.format(now);

        // Update date label
        dateLabel.setText(formattedDate);
    }
    

