    private void jbtnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPrintActionPerformed
        MessageFormat header = new MessageFormat("Printing in progress");
        MessageFormat footer = new MessageFormat("Page {0, number, integer}");

        // Clear previous text
        jTextAreaPrint.setText("");

        // Add header
        String headerRow = String.format("%-4s %-30s %-10s %s\n", "Id", "Item", "Quantity", "Price");
        jTextAreaPrint.append("                        Coffee Lab\n" +
                             "-------------------------------------------------------------------\n" +
                             headerRow +
                             "-------------------------------------------------------------------\n");

        // Add items from jTable1
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            String id = model.getValueAt(i, 0).toString();
            String item = model.getValueAt(i, 1).toString();
            String quantity = model.getValueAt(i, 2).toString();
            String price = model.getValueAt(i, 3).toString();

            // Adjust the number of spaces based on your desired column width
            String row = String.format("%-4s %-30s %-10s %s\n", id, item, quantity, price);
            jTextAreaPrint.append(row);
        }

        // Add Sub Total, Cash, Balance, and footer
        String subTotal = String.format("\nSub Total: %-10s\n", jtxtSubTotal.getText());
        String cash = String.format("Cash: %-10s\n", jtxtDisplay.getText());
        String balance = String.format("Balance: %-10s\n", jtxtChange.getText());

        jTextAreaPrint.append("-------------------------------------------------------------------\n");
        jTextAreaPrint.append(subTotal);
        jTextAreaPrint.append(cash);
        jTextAreaPrint.append(balance);
        jTextAreaPrint.append("\n                    Thanks For Your Business...!\n");
        jTextAreaPrint.append("-------------------------------------------------------------------\n");

        try {
            jTable1.print(JTable.PrintMode.NORMAL, header, footer);
        } catch (java.awt.print.PrinterException e) {
            System.err.format("No Printer found", e.getMessage());
        }
    }//GEN-LAST:event_jbtnPrintActionPerformed

